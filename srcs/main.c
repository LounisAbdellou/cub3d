void	cub3d(t_env *env)
{
	env->win = mlx_new_window(env->mlx, env->screen_w, env->screen_h, "cub3d");
	if (!env->win)
		return_error("Error initializing window\n", 1, env);
	mlx_mouse_hide(env->mlx, env->win);
	mlx_hook(env->win, 6, 1L << 6, handle_mouse, env);
	// mlx_hook(env->win, 2, 1L << 0, handle_keydown, env);
	mlx_key_hook(env->win, handle_keydown, env);
	mlx_hook(env->win, 17, 1L << 0, destroy, env);
	render(env);
	mlx_loop(env->mlx);
}

void	init_texture(t_env *env, t_texture *texture)
{
	texture->width = 64;
	texture->height = 64;
	texture->ptr = mlx_xpm_file_to_image(env->mlx, texture->path,
			&(texture->width), &(texture->height));
	texture->addr = mlx_get_data_addr(texture->ptr, &(texture->bits_per_pixel),
			&(texture->line_length), &(texture->endian));
}

int	main(int ac, char **av)
{
	t_env	env;

	(void)av;
	init_env(&env);
	env.player.pos.x = 2 * TILE_SIZE;
	env.player.pos.y = 2 * TILE_SIZE;
	env.player.angle = M_PI * 2;
	if (ac != 2)
		return_error("Wrong number of arguments\n", 1, &env);
	env.assets.n.path = ft_strdup("./assets/sprites/red.xpm");
	env.assets.s.path = ft_strdup("./assets/sprites/blue.xpm");
	env.assets.e.path = ft_strdup("./assets/sprites/green.xpm");
	env.assets.w.path = ft_strdup("./assets/sprites/yellow.xpm");
	init_texture(&env, &(env.assets.n));
	init_texture(&env, &(env.assets.s));
	init_texture(&env, &(env.assets.e));
	init_texture(&env, &(env.assets.w));
	cub3d(&env);
	return (0);
}
